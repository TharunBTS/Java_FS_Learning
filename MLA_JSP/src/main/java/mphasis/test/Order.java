package mphasis.test;

public class Order {
	
	private int id;
	private String name;
	private String descp;
	private int qty;
	
	
	
	public int getId() {
		return id;
	}
	
	public void setId(int id) {
		this.id = id;
	}
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public String getDescp() {
		return descp;
	}
	
	public void setDescp(String descp) {
		this.descp = descp;
	}
	
	public int getQty() {
		return qty;
	}
	
	public void setQty(int qty) {
		this.qty = qty;
	}
	

}
